export function getThemeModeVars(themeMode, accentTone, backgroundScene) {
    const theme = themeModes[themeMode] ?? themeModes.original;

    return {
        '--workspace-color-scheme': theme.scheme,
        '--workspace-canvas': theme.canvas,
        '--panel-core': theme.panelCore,
        '--panel-tint': theme.panelTint,
        '--panel-highlight': theme.panelHighlight,
        '--secondary-core': theme.secondaryCore,
        '--line-base': theme.lineBase,
        '--shadow-rgb': theme.shadowRgb,
        '--color-surface': theme.surface,
        '--color-surface-strong': theme.surfaceStrong,
        '--color-surface-soft': theme.surfaceSoft,
        '--color-panel': theme.panel,
        '--color-panel-strong': theme.panelStrong,
        '--color-line': theme.lineBase,
        '--color-ink': theme.ink,
        '--color-muted': theme.muted,
        '--workspace-backdrop': theme.backgrounds[backgroundScene] ?? theme.backgrounds.mist,
        ...(theme.accents[accentTone] ?? theme.accents.sereno),
    };
}
